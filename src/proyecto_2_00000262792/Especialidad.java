package proyecto_2_00000262792;

public class Especialidad {
    int id;
    String nombre;

    public Especialidad(){

    }

    public Especialidad(int id, String nombre){
        this.id = id;
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }
}
